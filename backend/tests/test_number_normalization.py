"""Tests for number expansion in text and the decimal-aware sentence splitter
(NUMBERS.md stages 1-3 + the D4 linguistic review corrections)."""

import pytest

from app.services import text_normalizer as tn


@pytest.mark.parametrize(
    "text,expected",
    [
        # --- Stage 1: cardinals, decimals, percent, tenge ---
        ("Бізде 1250 кітап бар", "Бізде мың екі жүз елу кітап бар"),
        ("5% жеңілдік", "бес пайыз жеңілдік"),
        ("1000 ₸", "мың теңге"),
        ("Баға 3,14", "Баға үш бүтін жүзден он төрт"),
        ("Баға 3.14", "Баға үш бүтін жүзден он төрт"),
        ("1 000 000 адам", "бір миллион адам"),
        ("-5 градус", "минус бес градус"),
        ("Сәлем әлем", "Сәлем әлем"),
        # --- Stage 2: ordinals / case suffixes ---
        ("5-ші орын", "бесінші орын"),
        ("21-ші", "жиырма бірінші"),
        ("10-ға дейін", "онға дейін"),
        ("100-ға", "жүзге"),
        ("5-тен", "бестен"),
        ("5-сынып", "бесінші сынып"),
        ("2015 жыл", "екі мың он бесінші жыл"),
        ("21 ғасыр", "жиырма бірінші ғасыр"),
        ("2 жыл бойы", "екі жыл бойы"),
        # Instrumental re-derived from the spelled word (D4):
        ("1000-мен салыстыр", "мыңмен салыстыр"),
        ("10-бен көбейт", "онмен көбейт"),       # written "бен" corrected to "мен"
        ("100-бен бөл", "жүзбен бөл"),
        ("50-ні таңдаңыз", "елуді таңдаңыз"),     # елу accusative -> елуді
        # --- Stage 3: dates (adverbial year -> "жылы") ---
        ("05.05.2024", "бесінші мамыр екі мың жиырма төртінші жылы"),
        ("31.12.1999", "отыз бірінші желтоқсан мың тоғыз жүз тоқсан тоғызыншы жылы"),
        ("05.05.2024 жыл", "бесінші мамыр екі мың жиырма төртінші жыл"),
        ("5 мамыр", "бесінші мамыр"),
        ("5 мамыр 2024 жыл", "бесінші мамыр екі мың жиырма төртінші жыл"),
        ("45.13.2024", "45.13.2024"),
        # Full date before "күні": year leads, in the genitive.
        (
            "Кездесу 01.01.2024 күні өтті.",
            "Кездесу екі мың жиырма төртінші жылдың бірінші қаңтар күні өтті.",
        ),
        # Adverbial date without a following year word -> "жылы".
        (
            "Есеп 31.12.2025 дайын болады.",
            "Есеп отыз бірінші желтоқсан екі мың жиырма бесінші жылы дайын болады.",
        ),
        # Day before an inflected month stays ordinal.
        (
            "Тексеру 2026 жылғы 10 шілдеде өтті.",
            "Тексеру екі мың жиырма алтыншы жылғы оныншы шілдеде өтті.",
        ),
        # --- Stage 3: time ---
        ("14:30", "он төрт отыз"),
        ("9:00", "тоғыз"),
        ("сағат 14:30", "сағат он төрт отыз"),
        ("сағат 14:30-да", "сағат он төрт отызда"),
        ("9:00-дан", "тоғыздан"),
        ("18:00-ге", "он сегізге"),
        # Minutes 01-09 keep the leading "нөл".
        ("Ұшу 14:05-ке жоспарланды.", "Ұшу он төрт нөл беске жоспарланды."),
        # Time range with an en dash -> "…бен…".
        (
            "Кеңсе 9:30–17:45 аралығында істейді.",
            "Кеңсе тоғыз отыз бен он жеті қырық бес аралығында істейді.",
        ),
        # --- Stage 3: phone ---
        (
            "+77011234567",
            "плюс жеті жеті нөл бір бір екі үш төрт бес алты жеті",
        ),
        # --- Stage 3: ranges ---
        ("5–10", "бестен онға дейін"),
        # Year range -> ordinal years with "жыл" declined; trailing "жылдары" eaten.
        (
            "Жоба 2020–2024 жылдары жүрді.",
            "Жоба екі мың жиырмасыншы жылдан екі мың жиырма төртінші жылға дейін жүрді.",
        ),
        # Range before "аралығында" -> "X мен Y" (no "дейін").
        (
            "Қатысушылар саны 120–150 аралығында.",
            "Қатысушылар саны жүз жиырма мен жүз елу аралығында.",
        ),
        ("2-1 есеп", "екі-бір есеп"),
        # --- D4 review: codes / PIN / currency / № / version ---
        ("Код 12345 түрінде енгізілді.", "Код бір екі үш төрт бес түрінде енгізілді."),
        ("ПИН 0000 деп қойылды.", "ПИН нөл нөл нөл нөл деп қойылды."),
        ("Баға 2500 ₸-ден басталады.", "Баға екі мың бес жүз теңгеден басталады."),
        ("№5 кабинет ашық.", "бесінші кабинет ашық."),
        ("Версия 2.0 бүгін шықты.", "Версия екі нүкте нөл бүгін шықты."),
    ],
)
def test_expand(text, expected):
    assert tn.expand_numbers_kk(text) == expected


def test_mixed_date_and_time_sentence():
    out = tn.expand_numbers_kk("Кездесу 05.05.2024, сағат 14:30-да")
    assert out == (
        "Кездесу бесінші мамыр екі мың жиырма төртінші жылы, "
        "сағат он төрт отызда"
    )


def test_decimal_not_a_sentence_break():
    assert [s["text"] for s in tn.split_sentences("3.14")] == ["3.14"]
    assert [s["text"] for s in tn.split_sentences("05.05.2024 жыл.")] == [
        "05.05.2024 жыл."
    ]


def test_decimal_split_still_finds_real_terminators():
    sentences = tn.split_sentences("Баға 3.14 теңге. Рахмет.")
    assert [s["text"] for s in sentences] == ["Баға 3.14 теңге.", "Рахмет."]


def test_trailing_dot_after_decimal_ends_sentence():
    # Internal dot is a decimal; the final dot is a real terminator.
    assert [s["text"] for s in tn.split_sentences("Цена 1.5.")] == ["Цена 1.5."]
